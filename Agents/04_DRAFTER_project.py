# Boss's Order

# Task: Our company is not working efficiently! We spend way too much time drafting documents and this needs to be fixed!

# For the company, you need to create an AI Agent System that can speed up drafting documents, emails, etc. The AI Agent System should have Human-AI Collaboration meaning the Human should be able to provide continuous feedback and the AI Agent should stop when the Human is happy with the draft. The system should be fast and be able to save the drafts.

